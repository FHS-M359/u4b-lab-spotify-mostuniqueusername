public class Song {
  private String title;
  private String artist;
  private String album;
  private int length;
  private int year;
  private String genre;

  public Song(String title, String artist, String album, int length, int year, String genre){
    this.title = title;
    this.artist = artist;
    this.album = album;
    this.length = length;
    this.year = year;
    this.genre = genre;
  }

  
}
